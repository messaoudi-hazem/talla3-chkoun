export type TranslationKey = keyof typeof translations.en;

export function t(key: string, lang: 'en' | 'ar', args?: Record<string, any>) {
  let str = (translations[lang] as any)[key] || (translations['en'] as any)[key] || key;
  if (args) {
    for (const [arg, val] of Object.entries(args)) {
      str = str.replace(`{${arg}}`, val);
    }
  }
  return str;
}

